<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Air pollution in Kolkata is one incredibly important, yet often overlooked
    public health issue. When in Kolkata, all you have to do is look up to
    notice the many contaminants in the air. The aforementioned
    industrialization and population density, combined with the many vehicles on
    the streets, the many factories and coal power plants, as well as frequent
    biomass/coal burning for cooking, all comes together to cause an atmosphere
    dangerous to everyone who lives there.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Particulate matter, especially PM<sub>2.5</sub> (particles smaller than 2.5
    micrometers) and PM<sub>10</sub> (particles smaller than 10 micrometers),
    constitute a lot of the pollutants in Kolkata’s air. These fine particles,
    also known as aerosols, are dangerous for many reasons. In European Union
    countries, PM<sub>2.5</sub> decreased the average life span by 8.6 months
    and the chance of respiratory mortality increased by 0.58% for every 10 µg/m<sup
        >3</sup
    >
    increase of PM<sub>10</sub>. Individuals with higher exposure have a 9%
    greater risk of death from cardiovascular causes, and the confidence
    intervals indicate the risk could range from no increase to 18% higher. A 10
    μg/m<sup>3</sup>
    increase in PM<sup>2.5</sup>
    exposure was associated with a 1.11 times higher risk of lung cancer mortality
    and a 1.08 times higher risk of lung cancer incidence with confidence intervals
    that indicate the risk could range from 3% to 18% higher. PM<sub>2.5</sub>
    and PM<sub>10</sub> are primarily released from industrial activities and
    construction. The total amount of PM<sub>2.5</sub> released in 2018 was
    88,250 tons, about 630 times higher than the baseline set by the Oregon
    Department of Environmental Quality (DEQ) of 140 tons annually, and the
    total amount of PM<sub>10</sub> was 134,750 tons, nearly 1500 times higher
    than the PM10 baseline of 90 tons.<sup>1,3,4,5,6,7</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    The chemical makeup of particulate matter in Kolkata is varied. It includes
    sulfates, which are formed from the oxidation of sulfur dioxide (SO<sub
        >2</sub
    >), mainly emitted from coal combustion and diesel-powered vehicles. The
    total amount of SO<sub>2</sub> released in 2018 was 10,400 tons, over 26
    times higher than the DEQ’s SO<sub>2</sub> baseline of 390 tons. Similarly,
    particulate matter also contains nitrates, which result from the reaction of
    nitrogen oxides (NO<sub>x</sub>) with atmospheric oxygen and water vapor. NO<sub
        >x</sub
    >
    is produced primarily by motor vehicles. The total amount of NO<sub>x</sub>
    in 2018 was 143,650 tons, almost 370 times higher than the DEQ’s NO<sub
        >x</sub
    >
    baseline of 390 tons. Additionally, it contains Organic Carbon (OC), which is
    produced by the incomplete combustion of fossil fuels, biomass/coal burning,
    and the smoking of tobacco, the total of which in 2018 was 15,400 tons; as well
    as Elemental Carbon (EC), commonly known as black carbon and emitted from diesel
    engines and coal power plants, the total of which in 2018 was 12,900 tons. The
    presence of these chemicals in the atmosphere makes particulate matter a dangerous
    component of air pollution, leading to the aforementioned medical issues.<sup
        >4,5,8,9</sup
    >
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    There also exists several toxic gasses in Kolkata's air, with significant
    health implications for its residents, the first of which is the
    aforementioned sulfur dioxide (SO<sub>2</sub>). Sulfur dioxide is produced
    by the combustion of sulfur-containing fuels, such as coal and petroleum.
    Power plants, factories, and vehicle emissions are major sources of SO<sub>
        2</sub
    >. SO<sub>2</sub> can irritate the respiratory tract and contribute to the
    formation of acid rain, which harms ecosystems and buildings.<sup>5</sup>
    The chemical formula for the formation of acid rain is 2SO<sub>2</sub> + O<sub
        >2</sub
    >
    + 2H<sub>2</sub>O → 2SO<sub>3</sub> + 2H<sub>2</sub>O → 2H<sub>2</sub>SO<sub
        >4</sub
    >, where sulfur dioxide, diatomic oxygen, and water react to make sulfuric
    acid (acid rain). The total amount of SO<sub>2</sub>
    released in 2018 was 10,400 tons, over 26 times higher than the DEQ’s SO<sub
        >2
    </sub>
    baseline of 390 tons.<sup>4,5,10</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Nitrogen oxides, which include nitrogen dioxide (NO<sub>2</sub>) and nitric
    oxide (NO), are primarily emitted by vehicles and power plants. These
    chemicals are not inherently dangerous by themselves, but NO<sub>2</sub>
    is a product in the formation of ozone, the chemical formula of which is NO<sub
        >x</sub
    >
    + VOCs [see below] + sunlight → O<sub>3</sub>. Ground-level ozone is a
    harmful oxidant, as a 10 ppb increase in ozone leads to a 4% increase in
    respiratory mortality, and ozone also increases risk of asthma by 10%. The
    total amount of NO<sub>x</sub> in 2018 was 143,650 tons, almost 370 times
    higher than the DEQ’s NO<sub>x</sub> baseline of 390 tons.
    <sup>4,5,11,12,13</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    The third and last gas present in Kolkata’s air is carbon monoxide produced
    by the incomplete combustion of fuels, particularly from motor vehicles and
    household stoves. CO interferes with the body's ability to transport oxygen,
    leading to fatigue, headaches, and, in severe cases, death, with a 1 mg/m<sup
        >3</sup
    >
    increase in the average CO concentration of the previous day being associated
    with a 0·91% increase in daily total mortality. There was a whopping 597,550
    tons of CO released in 2018, over 600 times the DEQ baseline of 990 tons.
    <sup>4,5,14,15</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Volatile organic compounds (VOCs) are a class of chemicals that can easily
    evaporate into the air, contributing to the formation of ground-level ozone
    and smog. Major sources of VOCs include vehicle emissions, paint, solvents,
    and biomass/coal burning. Examples include benzene (C<sub>6</sub>H<sub
        >6</sub
    >), toluene (C<sub>6</sub>H<sub>5</sub>CH<sub>3</sub>), formaldehyde (CH<sub
        >2</sub
    >O), and xylene ((CH<sub>3</sub>)<sub>2</sub>C<sub>6</sub>H<sub>4</sub>) are
    common VOCs found in the air. Long-term exposure to VOCs contributed to a
    lifetime cancer risk affecting between 0.60 million to 0.85 million
    individuals globally. The total amount of VOCs released into the air in 2018
    was 140,850 tons, over 360 times the DEQ baseline of 390 tons.
    <sup>4,5,16,17</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Ammonia is released primarily from agricultural activities, including the
    use of nitrogenous fertilizers. NH<sub>3</sub> is also emitted from
    wastewater treatment plants and the decomposition of organic waste. Ammonia
    in the air can react with sulfuric and nitric acids to form particulate the
    secondary aerosol of ammonium salts, which contribute to fine particulate
    matter in the air and exacerbate air pollution.
    <sup>18,19</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Lastly, Benzo(a)pyrene (BaP) is a polycyclic aromatic hydrocarbon (PAH) and
    is released into the atmosphere in smoke, from cigarettes, wildfires, and
    other causes, as well as the burning of coal. BaP is a Group I carcinogenic
    agent (matter that the International Agency for Research on Cancer (IARC)
    has classified as carcinogenic to humans, including substances like asbestos
    and tobacco) that is proven to increase the probability of lung cancer, skin
    cancer, and other forms of cancer by 11%. According to a study from the
    Asian Journal of Chemistry, the average BaP level in Kolkata’s air between
    1999 and 2011 was 29.82 ng/m<sub>3</sub>, a bit less than 30 times than the
    EU’s guideline of 1 ng/m<sub>3</sub> of BaP in the atmosphere.<sup
        >20,21,22,23,24</sup
    >
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Another thing to consider with air pollution, specifically in Kolkata, is
    temperature. The year-round average for Kolkata in 2023 was 82.6°F, which is
    very, very high. Air pollution levels spike when temperatures rise. High
    temperatures can lead to more frequent droughts and more intense wildfires,
    both of which increase particulate matter (PM<sub>10</sub>
    and PM<sub>2.5</sub>). Fires also release large amounts of EC, NO<sub>x</sub
    >, CO and VOCs. Heat also accelerates biological processes responsible for
    the degradation of organic waste and wastewater, releasing both air
    pollutants and greenhouse gases into the air. Warmer temperatures also
    accelerate the reactions that form ozone.<sup>25,26</sup>
</p>
<p class="text-white font-mono text-sm indent-8">
    Air pollution in Kolkata presents a severe and growing public health crisis,
    driven by industrial emissions, vehicular exhaust, biomass burning, and
    other sources. The presence of harmful particulate matter, toxic gases, and
    volatile organic compounds poses significant risks to the population,
    including respiratory and cardiovascular diseases, lung cancer, and
    increased mortality rates. The city's high levels of pollutants, far
    exceeding international environmental guidelines, exacerbate the health
    impacts on residents, with heat and temperature fluctuations further
    compounding the problem. Addressing Kolkata's air pollution requires urgent
    action to reduce emissions and mitigate the harmful effects on public health
    and the environment.
</p>
