<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    You can’t talk about water pollution in Kolkata without talking about the
    Ganges. The Ganges River (and its distributaries) is the most sacred river
    in India. Hindus believe that if one consumes water from the Ganga before
    their last breath will allow them to attain Moksha and ascend to Svarga.
    However, despite its holiness, the Ganges River is the most polluted river
    in the entire world. The distributary of the Ganges that runs through
    Calcutta is known as the Hooghly River, about 100 miles north of where the
    Hooghly empties into the Bay via the Mouths of the Ganges, the focus of this
    section.<sup>28,29</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Heavy metal contamination in the Hooghly River is a serious issue.
    Industrial byproducts, particularly from battery manufacturing, mining, and
    electroplating industries, discharge heavy metals into the water. Lead (Pb)
    is a toxic metal that has caused more than 1.5 million deaths globally,
    primarily due to cardiovascular effects and was estimated to account for
    more than 33 million years lost to disability worldwide in 2021. Lead levels
    in the Hooghly were 123.3 ppb (as of 2000, newer data could not be found), 8
    times higher than the EPA Maximum Guideline of 15 ppb. Cadmium (Cd) is toxic
    to aquatic life and bioaccumulates, a threat to both animals and humans who
    consume water or seafood with cadmium as it has been associated with a
    higher likelihood of developing chronic kidney disease (CKD), with some
    studies showing a notable effect at levels as low as 0.18 µg/L blood cadmium
    and 0.27 µg/g creatinine in urine. Additionally, cadmium exposure is
    significantly linked to anemia, with studies showing that increased blood
    cadmium levels are associated with higher red cell distribution width (RDW),
    indicating potential for hemolytic anemia, even after adjusting for iron
    deficiency. Cadmium as of 2000 were 28.9 ppb, nearly 6 times higher than the
    EPA Maximum Guideline of 5 ppb. Chromium (Cr) is acceptable in small
    concentrations, but in concentrations over 100 ppb (the EPA Maximum
    Guideline), hexavalent chromium (Cr(VI)) is considered a known human
    carcinogen, with studies showing a strong association between exposure to
    high levels of Cr(VI) and increased risk of lung cancer. Chromium levels as
    of 2000 were 143.3, a concentration that is very much not healthy.<sup
        >30,31,32,33,34,35,36,37</sup
    >
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Excessive nutrients, particularly nitrogen (N) and phosphorus (P), are
    commonly found in the Hooghly River. According to the EPA, an acceptable
    level of nitrogen in drinking water (measured as nitrate-nitrogen NO<sub
        >3</sub
    >N) is considered to be around 10 milligrams per liter (mg/L), which
    translates to approximately 161 µM. Mean NO<sub>3</sub>N concentrations in
    the Hooghly were 536.4±125.5 µM, four times the guidelines. The EPA
    recommends that drinking water has a phosphorus level of no more than 0.3864
    μg/m<sub>3</sub>, whereas the levels in the Hooghly were 40 μg/m<sub>3</sub
    >, roughly 100 times the acceptable baseline. The primary sources of
    nutrient pollution include untreated sewage, agricultural runoff containing
    fertilizers, and industrial discharges. High concentrations of nitrogen and
    phosphorus promote the rapid growth of algae, a process known as
    eutrophication. This leads to the depletion of oxygen in the water
    (hypoxia), causing the death of fish and other aquatic organisms, the
    chemical explanation of which is Nitrates (NO<sub>3</sub><sup>-</sup>) +
    Phosphates (PO<sub>4</sub><sup>3-</sup>) → Algal Blooms → Hypoxia. This is a
    big issue in Kolkata, both in the Hooghly River and in the East Kolkata
    Wetlands.<sup>38,39,40,41,42</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Lastly, organic pollutants, including industrial chemicals, pesticides, and
    pharmaceuticals, are prevalent in the Hooghly, specifically the pesticide
    dichlorodiphenyltrichloroethane (DDT) and a class of chemicals called PCBs
    (polychlorinated biphenyls). The WHO sets the baseline for DDT at 1 μg/L,
    whereas DDT in the Hooghly is approximately 8.97 μg/L, nearly nine times
    higher. The WHO also sets the baseline for PCBs at 10 μg/L, but the amount
    present in the Hooghly is an absolutely disappointing 1731 μg/L. These
    pollutants can have long-lasting effects on water quality and ecosystem
    health, and are not only toxic but also highly persistent in the
    environment. They accumulate in the food chain, affecting both wildlife and
    humans. Mammals exposed to DDT develop liver tumors and have an increased
    risk of liver tumors and DDT is a “likely human carcinogen” according to the
    IARC. Additionally, DDT can disrupt hormone systems and mothers who were
    exposed to DDT prior to age 14 years showed a 5-fold increase in risk of
    breast cancer. PCBs, once widely used in electrical equipment, are now
    banned but continue to pollute water bodies due to their long persistence.
    <sup>43,44,45,46,47</sup>
</p>
<p class="text-white font-mono text-sm indent-8">
    The Hooghly River, a vital water source and cultural symbol in Kolkata, is
    severely threatened by multiple forms of pollution, including heavy metals,
    excessive nutrients, and organic contaminants. The levels of lead, cadmium,
    chromium, nitrogen, phosphorus, and persistent chemicals like DDT and PCBs
    far exceed internationally accepted guidelines, posing serious health risks
    to both aquatic life and humans. The river's pollution is driven by
    industrial discharge, untreated sewage, and agricultural runoff, which
    collectively contribute to the degradation of water quality, disrupt
    ecosystems, and endanger public health. Addressing these issues requires
    urgent intervention, comprehensive monitoring, and stricter enforcement of
    environmental regulations to protect both the Hooghly River and the
    communities that depend on it.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Pollution in Kolkata is a multifaceted issue with significant chemical
    implications for air and water quality, with pollutants far exceeding the
    acceptable baselines set by credible environmental organizations like the
    EPA or the UN. The primary sources of pollutants in the city: industrial
    activities, vehicular emissions, agricultural practices, and untreated
    sewage release a range of harmful chemicals into the environment. The vast
    array of pollutants pose severe health risks, from respiratory and
    cardiovascular diseases to neurological disorders and cancer.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    There are a large amount of potential solutions to the pollution crisis in
    the region: shifting to cleaner fuels like green energy, robust
    implementation of pollution control plans, banning waste burning, improving
    sewage treatment, promoting awareness campaigns, and strictly enforcing
    emission norms for vehicles, all while monitoring air quality regularly to
    identify pollution hotspots. Effective pollution interventions include
    upgrading and expanding mass transit systems, reducing sulfur content in
    fuel, converting coal-fired power plants to natural gas, and redesigning
    urban areas to promote walkable communities. There really isn’t a satisfying
    be-all end-all solution to air or water pollution, and the only way these
    problems will be solved is through a multifaceted, systematic effort to
    improve society as a whole. Only through such collective action can Kolkata
    hope to mitigate the damaging effects of pollution and ensure a healthier,
    more sustainable future for its residents and the environment.
</p>
