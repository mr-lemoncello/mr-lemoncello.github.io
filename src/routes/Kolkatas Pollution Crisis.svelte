<p class="text-white font-mono text-sm indent-8 mb-0">
    In 2024, I had the opportunity to visit Kolkata, a city of historical,
    cultural, and personal significance. I was only there for a brief 48 hours.
    In those precious hours, I met with relatives for the first time, relatives
    I had not met in nearly a decade, and dying relatives who I will likely
    never meet again. But additionally, I found myself in a rather curious
    position: I was also deeply immersed in the mundane but critical task of
    analyzing the chemistry of pollution.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Kolkata certainly presents the strange juxtaposition of vibrant streets with
    a past of revolutionaries, poets, and intellectuals and the harsh reality of
    an over-industrialized city facing the long-term consequences of rapid
    urbanization. While exploring, I was surrounded by the signs of a city
    struggling with air and water contamination. While the very reason I was
    taking the trip was the death of my grandfather (and his return to the
    sacred Ganga), I was also tasked with examining the ever-important chemicals
    that cloud the air and poison the waters we depend on.
</p>
<p class="text-white font-mono text-sm indent-8">
    This report (which would be more aptly classified as a meta-analysis), comes
    not only from the perspective of a researcher examining pollutants, but also
    from someone deeply cognizant of the city’s past and present, and the
    delicate balance of heritage, progress, and environmental stewardship,
    someone with a ‘stake in the game,’ if you will. With that in mind, let us
    turn to the chemistry of pollution in Kolkata: the chemical nature of these
    pollutants, their sources, and their environmental and health impacts.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Kolkata (formerly Calcutta) is a city of immense historical significance in
    India, having served as the capital of British India until 1911. Founded in
    1690 by the British East India Company, it became a major center for trade
    and colonial administration. Over the 19th and early 20th centuries, Kolkata
    was at the heart of India’s intellectual and cultural renaissance, fostering
    revolutionary ideas, social reform, and artistic movements. Figures like
    Rabindranath Tagore, Swami Vivekananda, and Ishwar Chandra Vidyasagar helped
    shape a modern Indian identity that blended tradition and Western
    influences.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    The city was also a focal point for political resistance against British
    rule. Kolkata was home to several key revolutionary groups, such as the
    Anushilan Samiti, who launched acts of defiance against the British. The
    iconic Coffee House on College Street became a gathering place for
    intellectuals and left-wing activists, where debates on Marxism,
    nationalism, and politics were held regularly, and essentially the staging
    ground for the entire revolution in the late 19th Century. This café culture
    in Kolkata’s intellectual life was emblematic of the city’s role in shaping
    India’s political consciousness during the struggle for independence.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    After independence, Kolkata continued to be a center of political and
    cultural activity. The city became the stronghold of the Indian Communist
    movement and was the site of significant left-wing activism, including the
    Naxalite insurgency of the 1960s. Despite economic challenges in the
    post-colonial period, Kolkata remains a vibrant cultural hub, known for its
    literary heritage, cinema, and festivals like Durga Puja. Today, it balances
    its colonial past with a rich legacy of social reform, political engagement,
    and artistic expression.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Kolkata is renowned for its cultural heritage, vibrant arts scene, and
    colonial-era architecture. In the early 1900s, textile and jute factories
    led the city through a phase of rapid industrial development which also
    spurred the establishment of supporting infrastructure such as the rail
    transport systems, and communication infrastructure like the telegraph. It
    has since grown rapidly to become the 3rd most populated city in India. This
    rapid urbanization and industrialization in recent decades have led to
    significant environmental challenges, particularly in terms of pollution.<sup
        >1</sup
    >
</p>
<p class="text-white font-mono text-sm indent-8">
    As one of India’s most industrialized cities, Kolkata faces serious issues
    with air and water contamination, which have adverse effects on public
    health and the environment. The primary sources of these pollutants include
    vehicular emissions, industrial discharges, untreated sewage, and
    agricultural runoff. From a chemistry standpoint, pollution in Kolkata is
    driven by a variety of hazardous chemical substances, all of which pose
    significant risks to both human and ecological health.
</p>
