<p class="text-white font-mono text-sm indent-8 mb-0">
    George Orwell (born Eric Arthur Blair June 25th, 1903) was an English writer
    whose works include novels, poems, essays, journalism, prose, and social
    criticism. He is best known for the allegorical fable-novella Animal Farm
    (1945) and the dystopian novel Nineteen Eighty-Four (1949).<sup>1,2</sup>
    In 1984, Nineteen Eighty-Four won the Prometheus Award for its contributions
    to dystopian literature along with Ray Bradbury’s Fahrenheit 451, and in 2011,
    Animal Farm won. In 2003, Nineteen Eighty-Four was listed at number 8 and Animal
    Farm at number 46 on the BBC’s The Big Read poll, and in 2008, The Times ranked
    George Orwell as the second-best British writer since 1945.<sup>3,4</sup> In
    both Animal Farm and Nineteen Eighty-Four, George Orwell critiques authoritarianism’s
    manipulation of truth and disintegration of freedom with a clear warning that
    continues to remain relevant.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Both pieces are critical of authoritarian regimes and written in response to
    events recent at the time. Animal Farm is critical of communism and written
    in response to the rise of the Soviet Union; Nineteen Eighty-Four is
    critical of totalitarian regimes and written in response to growing
    totalitarian governments in Nazi Germany and Stalin’s Russia.<sup>5</sup>
    In Animal Farm, Orwell satirizes Russian Communism as it developed under Stalin
    through phrases like “In future all questions relating to the working of the
    farm would be settled by a special committee of pigs, presided over by himself.
    These would meet in private and afterwards communicate their decisions to the
    others,” showing how communism both in Animal Farm and in Russia adapted to be
    so different from its stated purpose that it ended up largely similar to the
    very regime it intended to establish,<sup>6</sup>
    and “four legs good, two legs bad,” showing the sheer ignorance and complacency
    of the public for believing in the regime.<sup>7</sup>
    Both Animal Farm and Nineteen Eighty-Four emerged as direct responses to the
    events of his time. Animal Farm was published in 1945, at a time when the Soviet
    Union was emerging as a global superpower, and its portrayal of the corrupting
    influence of power was a pointed critique of the rise of Joseph Stalin. The novel
    symbolizes the Russian Revolution and the eventual betrayal of its ideals. Initially,
    the desire for equality and freedom drives the animals to revolt; however, as
    the pigs, led by Napoleon, seize control, they gradually betray the original
    principles of the revolution. Perhaps the most famous phrase from the novella,
    “There was nothing there now except a single Commandment. It ran: ALL ANIMALS
    ARE EQUAL BUT SOME ANIMALS ARE MORE EQUAL THAN OTHERS,” is a sharp critique of
    how the Revolution became indistinguishable from the oppressive systems it sought
    to overthrow.<sup>8</sup>
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    Nineteen Eighty-Four, published four years later in 1949, takes Orwell’s
    critique of authoritarianism to a darker extreme. Nineteen Eighty-Four is
    more about the dangers of totalitarianism on a broader scale, which Orwell
    depicted using the fictional society of Oceania, specifically its extreme
    surveillance and control. The government, known only as the ‘Party’ and led
    by the mysterious and (reportedly) all-knowing figure of Big Brother,
    monitors every aspect of citizens’ lives, who has now become synonymous with
    oppressive surveillance and control in popular culture. The novel explores
    the ways in which totalitarian governments manipulate not just the material
    but the very concept of reality as well. As the protagonist, Winston Smith
    tries to rebel, but he is confronted with the terrifying power of the Party,
    which is able to redefine truth itself. The novel discussed the rampant
    psychological manipulation, propaganda, and obliteration of truth
    characteristic of totalitarian regimes, tactics all in service of a regime
    that controls not just the actions of its citizens but also their thoughts.
    The Party’s slogan is “War is Peace, Freedom is Slavery, Ignorance is
    Strength” and is an ironic and satirical illustration of the ideology of a
    totalitarian regime.<sup>9</sup>
    ‘Thoughtcrime,’ the idea that the most dangerous form of rebellion is not physical
    but mental, is central to the Party’s control. The novel’s plot is based around
    ‘doublethink,’ the ability to hold two contradictory beliefs at the same time,
    essential to survival in Nineteen Eighty-Four; one of the quotes illustrating
    this is “The Ministry of Peace concerns itself with war, the Ministry of Truth
    with lies, the Ministry of Love with torture and the Ministry of Plenty with
    starvation. These contradictions are not accidental, nor do they result from
    ordinary hypocrisy: they are deliberate exercises in doublethink,” and this inversion
    of truth is inherent in the propaganda set forth by many totalitarian regimes.<sup
        >10</sup
    > In the novel, the inversion manifests through the fabricated language of Newspeak,
    designed to manipulate and limit the scope of thought, making dissent impossible.
    The focus on language and its ability to control what people think says a great
    deal about Orwell’s belief in its power.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    What makes Orwell’s work so enduring is not just the historical context in
    which it was written, but its universality in its themes. Both books speak
    of the dangers of unchecked power, the manipulation of truth, and the
    erosion of individual freedoms: freedom of speech and religion, freedom to
    privacy, and the loss of humanity in general, all concerns that transcend
    any one time or place. With the advent of mass surveillance technologies and
    the rise of state-sponsored censorship, the potential for governments to
    exert total control over their citizens is as present as ever. Globally,
    authoritarianism is on the rise, as evident by a military-led coup in
    Myanmar; parliament dismissed in Tunisia; a cabinet reshuffle leading to the
    second coup in 10 months in Mali; jailing of political opponents in
    Nicaragua, Iran, and Russia; no-contest elections in Syria, Kazakhstan, and
    Uzbekistan; power grabs in Sudan, Chad, and Guinea; and the populist and
    authoritarian tendencies by president of the United States, Donald Trump.<sup
        >11</sup
    >
    The Trump administration plans to accomplish its goals through three methods
    of control which are illustrated in the writings of Orwell. The first is the
    decimation of freedom of speech, which Trump has gone about in a variety of ways,
    one of the most crucial is control of the media. Before taking office, Trump
    sued ABC and CBS News over coverage he deemed defamatory,<sup>12,13</sup>
    a tactic not used before by any president and a canary in a coalmine for the
    future of press freedom in the United States. Trump barred the Associated Press,
    a pillar of White House coverage for more than a century,<sup>14</sup>
    from the Oval Office and Air Force One for refusing to use "Gulf of America"
    instead of "Gulf of Mexico" after he made the change by decree. The third-largest
    newspaper by circulation in the United States is the Washington Post,<sup
        >15</sup
    >
    which is outright owned by one of Trump’s biggest allies, Jeff Bezos.<sup
        >16,17</sup
    >
    The Washington Post recently removed their editorials,<sup>18</sup>
    meaning that the writer’s perspectives are no longer represented and the news
    is left only to what is approved by higher-ups in the organization. Also, after
    Trump’s inauguration, they changed their slogan from “Democracy Dies in Darkness”
    to “Riveting Storytelling for All of America,” indicative of their political
    affiliations.<sup>19</sup> But perhaps Trump’s greatest method of
    controlling the media is just him talking: his claims of “fake news” have
    caused large swaths of the public to lose trust in mainstream media, with
    trust among Republicans in national news organizations plummeted from 70% in
    2016 to just 35% in 2021, correlating with Trump's persistent media
    criticisms,<sup>20</sup>
    only worsening in his second term.<sup>21</sup>
    In Animal Farm, one of the propaganda machines of Napoleon’s regime is Squealer,
    seen in “Squealer spoke so persuasively, and the three dogs who happened to be
    with him growled so threateningly, that they accepted his explanation without
    further questions.”<sup>22</sup> The result of this propaganda is obvious:
    “And yet the animals never gave up hope. Moreover, they never lost, even for
    an instant, their sense of honour and privilege in being members of Animal
    Farm. They were still the only farm in the whole county — in all England! —
    owned and operated by animals….Their hearts swelled with imperishable pride”<sup
        >23</sup
    >and “‘Ah, that is different!’ said Boxer. ‘If Comrade Napoleon says it, it
    must be right… Napoleon is always right.’”<sup>24</sup> In Nineteen
    Eighty-Four, the Ministry of Truth spreads Party-approved ideas and goes so
    far as to control the very notion of truth, including Winston Smith’s job at
    the Ministry of rewriting historical newspapers to align with the Party’s
    ideals. All three of these scenarios are situations where an institution
    that spreads the news only prints news that is approved or aligns with the
    ideals of the ruling party in a way that is able to convince the masses of
    those ideals in a way they believe to be true. Along with his blatant
    disregard for the truth,<sup>25</sup>the other way that Trump decimates free
    speech is by punishing and deporting dissenters. On March 8, 2025, Mahmoud
    Khalil, a student activist at Colombia University and lead negotiator for
    the encampment in the 2024 Colombia University pro-Palestinian campus
    occupations, was taken from his home by U.S. Immigration and Customs
    Enforcement (ICE) agents acting on orders from the State Department. Though
    Khalil has a green card, is married to a U.S. citizen, and is a lawful
    permanent resident of the United States, Colombia revoked his student visa,
    ICE revoked his permanent resident status, and he was transported to LaSalle
    Detention Center in Jena, Louisiana.<sup>26,27,28</sup>
    The detention was the first publicly known deportation effort related to pro-Palestine
    activism under Trump, who has threatened to punish those he says support Hamas
    or promote antisemitism.<sup>29</sup> On the night of March 8, hours before
    the detainment of Mahmoud Khalil, ICE knocked on Ranjani Srinivasan’s door
    for the second time in two days. The week prior, the State Department
    revoked her student visa for merely attending a pro-Palestine protest, which
    they deemed to be terrorist sympathizing. Srinivasan was not home, but
    shortly thereafter, she packed her belongings and fled to Canada.<sup
        >30</sup
    >
    On March 24, a French scientist was denied entry to Houston after U.S.
    customs officers found messages criticizing President Donald Trump's cuts to
    science funding on her phone.<sup>31</sup>
    In Animal Farm, Snowball is exiled from the farm and in Nineteen Eighty-Four,
    Winston Smith is brutally tortured for months by the Ministry of Love for accepting
    a book that upon completion, would grant him possibility of entry into the Brotherhood
    (the principal enemy of the Party). In all of these scenarios, free speech has
    been denied by making it clear dissent will be prosecuted, granting the leader
    immense power over his subjects. Under the Trump administration, not just leading
    a protest but even participating in one or criticizing him in any form is grounds
    for removal from the U.S. The second method of control that Trump uses to further
    his goals is the decimation of rule of law. He has issued a few executive orders
    that pervert the election process, making it easier to vote for him and harder
    for others through various methods of voter disenfranchisement.<sup>32</sup>
    Additionally, he has exploited one of the parts of the carefully crafted
    system of checks and balances meant to keep such a thing from happening
    (executive orders) to accomplish his goals in all manner of illegality.<sup
        >33</sup
    >
    For example, law firms in the U.S. have been presented with an ultimatum:
    either conform to his future policy decisions by agreeing not to sue over
    their unconstitutionality, or lose their resources or ability to exist.<sup
        >18</sup
    >
    In Animal Farm, Snowball, a political opponent, is exiled and then
    continually scapegoated for months to come, “Our Leader, Comrade Napoleon,”
    announced Squealer, speaking very slowly and firmly, “has stated
    categorically… that Snowball was Jones’s agent from the very beginning… yes,
    and from long before the Rebellion was ever thought of” and the commandments
    are progressively changed to nearly their opposite. In Nineteen Eighty-Four,
    there was no rule of law in the first place, only the Ministry of Love and
    its ruthless torture systems. Again, in all of these scenarios, rule of law
    has been denied such that the only laws that exist (or at least the most
    powerful ones) are the ones put in place by the leader and the leader alone,
    granting him even more power. The final method of control that Trump uses to
    accomplish his goals is control of financials. Trump has many of the
    wealthiest people and organizations in the United States funding him,
    including Timothy Mellon, Richard & Elizabeth A. Uihlien, Sheldon G. &
    Miriam O. Adelson, and Elon Musk, among others, who donated nearly a billion
    dollars to his campaign.<sup>34</sup> Additionally, he, through the first method
    of control, has the money of the American taxpayer behind him, and those alone
    combined make him one of the most powerful people on the planet, not to mention
    he also is the President of the United States of America. Animal Farm’s equivalent
    to currency is the manipulation of resources and privileges. Napoleon controls
    the resources on the farm by selectively distributing them to maintain loyalty
    and power, especially through the manipulation of food and supplies to ensure
    the animals remain subservient. In Nineteen Eighty-Four, the Party controls the
    economy through the Ministry of Plenty, which regulates all production and allocation,
    ensuring that citizens are always in a state of need and dependent on the Party
    for their survival. The Party’s ability to control financials and basic goods
    ensures a constant cycle of poverty and subjugation. In all of these scenarios,
    the leader has managed to gain control over financials, granting him an obscene
    amount of power on its own, not to mention the other two methods of control,
    making him an individual unmatched in power. Through these three methods of control,
    our freedoms are being systematically decimated at a breakneck pace just like
    the freedoms of the masses of Animal Farm and Nineteen Eighty-Four. George Orwell’s
    works are a warning from the past. He saw what was happening in his time for
    what it was and sought to give future generations the tools such that it wouldn’t
    happen again, and by understanding the writings, we can extrapolate a solution
    to combating the methods of control. In times where authoritarianism is on the
    rise, before it can become irreversible, we should recognize the tactics used
    by such regimes and how they can slowly creep into everyday life. Orwell’s writing
    warns us of the ways in which propaganda distorts the truth and how critical
    it is to defend objective facts in the face of manipulation. His works emphasize
    the importance of independent institutions, like the press, the judiciary, and
    civil society, that must remain free from the control of any single individual
    or party. Preserving our democracy requires more than just fighting against Trump;
    additionally, it requires fighting for the systems that help to support it, the
    very ones Trump has been going after. The effort demands constant vigilance against
    the gradual erosion of freedoms. Ultimately, Orwell shows us that even in the
    face of overwhelming power, the collective strength of mind and voice, if both
    committed to truth and justice, can resist the tactics to remove freedoms, protect
    the tenets of democracy, and ultimately fight against the spread of authoritarianism.
    Only through vigilant defense of our rights can we prevent history from repeating
    itself.
</p>
<p class="text-white font-mono text-sm indent-8 m-0 pt-0">
    The legacy of George Orwell is one of political insight and moral clarity.
    Through his depiction of communist regimes in Animal Farm and totalitarian
    ones in Nineteen Eighty-Four, Orwell not just explored the nature of power
    and the manipulation of reality but gave a clear warning about the
    consequences of unchecked authority that will continue to persist for the
    foreseeable future. As long as the corrupting influence of power continues
    to develop dictatorship and authoritarianism in the world, Animal Farm and
    Nineteen Eighty-Four will remain relevant in the fight for democracy, truth,
    and freedom. Make Orwell fiction again!
</p>
